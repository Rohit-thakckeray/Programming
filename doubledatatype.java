public class doubledatatype
{
    public static void main(String []args)
    {
    //double data type store Decimal value can store also not only real number
    double x = 5;
    double y = 2;
    double z = x / y;
    System.out.println(z);

    //double data type operation basic 
    double a1 = 5 ;
    double b1 = 2;
    System.out.println(a1+b1);
    System.out.println(a1-b1);
    System.out.println(a1*b1);
    System.out.println(a1/b1);

    //calculating a percentage of 5 subject 
    double math = 90;
    double phy = 91;
    double chem =92;
    double bio = 93;
    double funda = 94;
    double total=(math+phy+chem+bio+funda)/5;
    System.out.println("Your percentage is a "+total);

    //int data type store only real value not a point value 
    int a = 5;
    int b = 2;
    int c = a / b;
    System.out.println(c);

    //float data type store decimal value 
    float r = 5;
    float s = 2;
    float t = r / s;
    System.out.println(t);

    }
}

