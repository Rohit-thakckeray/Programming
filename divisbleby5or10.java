//to check 5 or 10 are divisble or not //
import java.util.Scanner;
public class divisbleby5or10
{
public static void main(String args[])
{
Scanner sc = new Scanner(System.in);
System.out.println("Enter one number :");
int inputnumber = sc.nextInt();
if(inputnumber%10==0 && inputnumber%10==0)
System.out.println("number is divisble by 5 or 10 :");
else
System.out.println("number is not divisble by 5 or 10 :");

}
}
